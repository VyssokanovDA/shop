        $single = Product::find($id);
        $category = Category::find($single->category_id);
        $brand = Brand::find($single->brand_id);
        //dd($category);
        return view('shop.single', [
            'users' => $users,
            'single' => $single, //получаем единственную запись в помощью find по id
